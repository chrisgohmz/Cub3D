#include "../includes/cub3d.h"

void	print_map(t_data *data)
{
	int	i;
	int	j;
	
	i = 0;
	while (i < 5)
	{
		j = 0;
		while (j < 6)
		{
			printf("%c ", data->map[i][j]);
			j++;
		}
		i++;
		printf("\n");
	}	
}
